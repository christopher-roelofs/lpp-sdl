#include <stdlib.h>
#include <string.h>
#include "luaplayer.h"

// SDL Port: All microphone functions are stubbed out since microphone is not essential for basic LPP operation
// These functions return appropriate default values to prevent Lua script errors

static int lua_start(lua_State *L) { return 0; }
static int lua_stop(lua_State *L) { return 0; }
static int lua_isRecording(lua_State *L) { lua_pushboolean(L, 0); return 1; }
static int lua_pause(lua_State *L) { return 0; }
static int lua_resume(lua_State *L) { return 0; }
static int lua_save(lua_State *L) { return 0; }

//Register our Microphone Functions
static const luaL_Reg Mic_functions[] = {
  {"start",         lua_start},
  {"stop",          lua_stop},
  {"isRecording",   lua_isRecording},
  {"pause",         lua_pause},
  {"resume",        lua_resume},
  {"save",          lua_save},
  {0, 0}
};

void luaMic_init(lua_State *L) {
	lua_newtable(L);
	luaL_setfuncs(L, Mic_functions, 0);
	lua_setglobal(L, "Mic");
}