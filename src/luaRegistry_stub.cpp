#include <stdlib.h>
#include <string.h>
#include "luaplayer.h"

#define stringify(str) #str
#define VariableRegister(lua, value) do { lua_pushinteger(lua, value); lua_setglobal (lua, stringify(value)); } while(0)

// SDL Port: All registry functions are stubbed out since system registry is not essential for basic LPP operation
// These functions return appropriate default values to prevent Lua script errors

static int lua_getKey(lua_State *L) { lua_pushstring(L, ""); return 1; }
static int lua_setKey(lua_State *L) { return 0; }

//Register our Registry Functions
static const luaL_Reg Registry_functions[] = {
  {"getKey",    lua_getKey},
  {"setKey",    lua_setKey},
  {0, 0}
};

void luaRegistry_init(lua_State *L) {
	lua_newtable(L);
	luaL_setfuncs(L, Registry_functions, 0);
	lua_setglobal(L, "Registry");
}